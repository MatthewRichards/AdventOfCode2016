    private static void Main8()
    {
      var emptyGrid = new bool[6].Select(_ => new bool[50]).ToArray();

      var result = Input
        .SplitLines()
        .Select(Parse)
        .Aggregate(emptyGrid, (grid, transform) => transform(grid))
        .ToList();

      result.ForEach(row => Console.WriteLine(string.Join("", row.Select(cell => cell ? "#" : "."))));

      Console.WriteLine(result.Sum(row => row.Sum(cell => cell ? 1 : 0)));

      Console.ReadKey();
    }

    private static Func<bool[][], bool[][]> Parse(string command)
    {
      var match = Parser.Match(command);

      if (match.Groups["rect_x"].Success)
      {
        return FillRectangle(int.Parse(match.Groups["rect_x"].Value), int.Parse(match.Groups["rect_y"].Value));
      }

      if (match.Groups["rotate_type"].Value == "column")
      {
        return RotateColumn(int.Parse(match.Groups["rotate_index"].Value), int.Parse(match.Groups["rotate_by"].Value));
      }

      return RotateRow(int.Parse(match.Groups["rotate_index"].Value), int.Parse(match.Groups["rotate_by"].Value));
    }

    private static Func<bool[][], bool[][]> FillRectangle(int x, int y)
    {
      return grid => grid.Select(
        (row, rowIndex) => row.Select(
          (cell, colIndex) => (rowIndex < y && colIndex < x) || cell).ToArray()).ToArray();
    }

    private static Func<bool[][], bool[][]> RotateColumn(int column, int by)
    {
      return grid => grid.Select(
        (row, rowIndex) => row.Select(
          (cell, colIndex) => column == colIndex ? grid[(grid.Length + rowIndex - by) % grid.Length][colIndex] : cell).ToArray()).ToArray();
    }

    private static Func<bool[][], bool[][]> RotateRow(int targetRow, int by)
    {
      return grid => grid.Select(
        (row, rowIndex) => rowIndex != targetRow ? row : row.Select(
          (cell, colIndex) => grid[rowIndex][(row.Length + colIndex - by) % row.Length]).ToArray()).ToArray();
    }

    private static readonly Regex Parser =
      new Regex(
        @"(rect (?<rect_x>\d+)x(?<rect_y>\d+)$)|(rotate (?<rotate_type>column|row) (x|y)=(?<rotate_index>\d+) by (?<rotate_by>\d+))$",
        RegexOptions.Compiled);

